//      - Loop first execute then condition will check
//      - Loop Execute again & again

let i = 3
do{
    // console.log(i) // Return - 3 -> 2 -> 1
    i--;
}while(i)

let j = 0
do{
    // console.log(j);
    j++;
}while(j<3);


