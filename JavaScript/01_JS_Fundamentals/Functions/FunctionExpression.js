/// Function Expression
//        - It allows us to create a new function in the middle of any expression. 


/// Function Declaration
function sayHi() {
  console.log("Hello");
}


/// Function Expression : Created at the middle of the Expression
let sayHi = function () { // also called a enonymous function assigned to variable 
  console.log("Hello");
};  // This ";" is part of Assignement Syntax